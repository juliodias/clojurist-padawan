(str "Hello World!")                                        ; "Hello World!"

(vector 1 2 3 4 5 6)                                        ; [1 2 3 4 5 6]

(list "J" "U" "L" "I" "O")                                  ; ("J" "U" "L" "I" "O")

(hash-map :julio "Julio" :dias "Dias")                      ; {:dias "Dias", :julio "Julio"}

(hash-set :a :a :b)                                         ; #{:b :a}