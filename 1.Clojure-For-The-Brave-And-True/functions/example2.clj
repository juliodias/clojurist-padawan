  A function is defined with 5 parts:
  1. defn
  2. Function name
  3. Optional function description
  4. Parameters listed in brackets
  5. Function Body)

(defn too-enthusiastic
  "Return too enthusiastic message to given name"
  [name]
  (str "OH. MY. GOD! " name " YOU ARE MOST DEFINITELY LIKE THE BEST
  MAN SLASH WOMAN EVER I LOVE YOU AND WE SHOULD RUN AWAY SOMEWHEERE"))

